#转义符 ：
print("my name is\"HDJ\"Goodnameisfordoggy")
print("\\")                       # \加字符->输出该字符
print("张\n三\n疯")                # \n换行
print("hello\tooo")
print("hello    ooo")             # \t制表符 代表4个空格 /补齐4位
print("I love\rpython")
print("I love\ryou")              # \r覆盖 只输出其后字符串
print("hello\bpython")            # \b->键盘 Backspace
print(r"张\n三\n疯")               # 张\n三\n疯
print(R"张\n三\n疯")               # 张\n三\n疯 #r/R 源字符可使转义符失效

