#元组&&列表&&字符串的共同操作
 #元组
a = (1, 2, 3, 4)
print(len(a))              # 4
print(max(a))              # 4
print(min(a))              # 1
 #列表
b = [10, 20, 30, 40]
print(len(b))              # 4
print(max(b))              # 40
print(min(b))              # 10
 #字符串
c = "12345 我 ABC"
print(len(c))              # 11
print(max(c))              # 我
print(min(c))              # 空格