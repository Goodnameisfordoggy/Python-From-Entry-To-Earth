#extend函数用于在列表的末尾添加新的列表(与append函数相比 extend函数可以一次性添加多个元素)
a = [10, 20, 30]
a.extend([1, 2, 3])
print(a)                # [10, 20, 30, 1, 2, 3]
a = [10, 20, 30]
a.append([1, 2, 3])
print(a)                # [10, 20, 30, [1, 2, 3]]
 #注意:(使用extend函数和列表加法的结果是一样的.但是extend函数是将新列表并入当前列表,而列表加法是返回新的列表.由此可见extend函数能够节约内存空间,更适用于大列表的链接操作)
a = [10, 20, 30]
b = [1, 2, 3]
c = a + b
print(c)                # [10, 20, 30, 1, 2, 3]