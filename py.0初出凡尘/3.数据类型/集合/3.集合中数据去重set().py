#数据去重
  #列表
aaa = set([1, 2, 1, 2, 1])
print(aaa)                  # {1, 2}
  #元组
bbb = set((1, 1, 1, 1,))
print(bbb)                  # {1}
  #字典
ccc = {
      "名字": "Goodnameisfordoggy",
      "名字": "HDJ"
  }
print(set(ccc))             # {'名字'}
  #字符串
ddd = set("1212121211")
print(ddd)                  # {'1', '2'}
 #用大括号去重
fff = {"1212121211", 1, 2, 1, (1, 2, 3)}
print(fff)                  # 1, 2, (1, 2, 3), '1212121211'} #使用{}只会对数字进行去重 ,对于元组,列表等整体元素会将其整体保留
