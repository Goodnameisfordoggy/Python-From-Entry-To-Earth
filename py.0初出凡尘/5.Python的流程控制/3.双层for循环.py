#Python的双层for循环
a = [1, 2, 3, [10, 20, 30], [40, 50, 60]]
  #需求:逐个输出列表中的元素
 #第一层for
for i in a:
    print(i)    # 1/2/3/[10, 20, 30]/[40, 50, 60]
print("=========================================================")
 #第二层for
for i in a:
    #判断是否为列表
    if isinstance(i,list):
        for x in i:
            print(x)
    else:
        print(i)     # 1/2/3/10/20/30/40/50/60